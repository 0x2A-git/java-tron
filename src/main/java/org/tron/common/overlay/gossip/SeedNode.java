package org.tron.common.overlay.gossip;

public class SeedNode {
    // gossip node id: 127.0.0.1:10000
    private String id;

    // gossip node uri: udp://127.0.0.1:10000
    private String uri;

    public SeedNode() {
    }

    public SeedNode(String id, String uri) {
        this.id = id;
        this.uri = uri;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUri() {
        return uri;
    }

    public void setUri(String uri) {
        this.uri = uri;
    }
}
